package Day2_09052020;

public class Linear_Array {
    public static void main(String[] args) {
        //linear string array example
        String[] username = new String[]{"Atik","Naim","Sumon","Kenneth"};

        //print out 2nd value from string array
        System.out.println("The second username is " + username[1]);

        //linear integer array example
        int[] numbers = new int[]{1,200,25,3000};

        //print out 1st and 3rd value from integer array
        System.out.println("The first number is " + numbers[0] + " and the third number is " + numbers[2]);

        //I want to get the sum of first 2 values
        System.out.println("The sum of the first two values is " + (numbers[0] + numbers[1]));

    }//end of psvm
}//end of class
